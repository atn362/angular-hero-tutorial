// assignment operator (=)
let x = 10
x = 20
console.log(x)

// addition assignment operator
let y = 10
y += 10
console.log(y)

// subtraction assignment operator
let z = 20
z -= 10
console.log(z)

// multiplication assignment operator
let foo = 5
foo *= 2
console.log(foo)

// division assignment operator
let bar = 20
bar /= 2
console.log(bar)

// remainder assignment operator
let baz = 5
baz %= 2
console.log(baz)

// exponentiation assignment operator
let myVar = 4
myVar **= 4
console.log(myVar)