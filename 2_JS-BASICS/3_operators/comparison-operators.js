// equal to
console.log(
    4 == 4
)
console.log(
    4 == '4'
)

// equal to and equal type
console.log(
    4 === 4
)
console.log(
    4 === '4'
)

// not equal
console.log(
    4 != '4'
)

// not equal type or not equal value
console.log(
    4 !== '4'
)

// greater than
console.log(
    4 > 3
)

// less than
console.log(
    3 > 4
)

// greater than or equal to
console.log(
    3 >= 4
)

// less than or equal to
console.log(
    3 <= 4
)

// ternary
console.log(
    3 <= 4 ? 'yes' : 'no'
)