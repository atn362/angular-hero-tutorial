// number
console.log(
    typeof 4
)

// string
console.log(
    typeof 'hello'
)

// boolean
console.log(
    typeof true
)

// undefined
let foo;
console.log(
    typeof foo
)

// null (defined nothingness)
let bar = null;
console.log(
    typeof bar
)

// NaN (Not a number)
console.log(
    typeof 50 / '5'
)